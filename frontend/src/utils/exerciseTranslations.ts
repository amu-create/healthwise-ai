// 운동 이름 번역 함수
export const translateExerciseName = (koreanName: string, language: string): string => {
  const translation = exerciseTranslations[koreanName];
  if (!translation) {
    return koreanName; // 번역이 없으면 원본 반환
  }
  
  switch (language) {
    case 'ko':
      return translation.ko;
    case 'en':
      return translation.en;
    case 'es':
      return translation.es;
    default:
      return koreanName;
  }
};

// 레벨 번역 함수
export const translateLevel = (level: string, t: any): string => {
  switch (level.toLowerCase()) {
    case '초급':
    case 'beginner':
    case 'nivel: 초급':
      return t('pages.aiWorkout.beginner');
    case '중급':
    case 'intermediate':
    case 'nivel: 중급':
      return t('pages.aiWorkout.intermediate');
    case '고급':
    case '상급':
    case 'advanced':
    case 'nivel: 고급':
    case 'nivel: 상급':
      return t('pages.aiWorkout.advanced');
    default:
      return level;
  }
};

// 근육 그룹 번역 함수
export const translateMuscleGroup = (muscleGroup: string, t: any): string => {
  const muscleGroupMap: { [key: string]: string } = {
    '가슴': 'chest',
    '등': 'back',
    '하체': 'legs',
    '어깨': 'shoulders',
    '팔': 'arms',
    '복근': 'abs',
    '전신': 'fullbody'
  };
  
  const key = muscleGroupMap[muscleGroup];
  if (key) {
    return t(`pages.aiWorkout.muscleGroups.${key}`);
  }
  
  return muscleGroup;
};

