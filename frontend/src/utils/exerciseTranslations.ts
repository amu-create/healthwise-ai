// 루틴 이름 번역 함수
export const translateRoutineName = (name: string, t: any): string => {
  // "부위 난이도 루틴" 패턴 처리
  const routinePattern = /^(.+)\s+(초급|중급|상급)\s+루틴$/;
  const match = name.match(routinePattern);
  
  if (match) {
    const [, muscleGroup, level] = match;
    const translatedMuscleGroup = translateMuscleGroup(muscleGroup, t);
    const translatedLevel = translateLevel(level, t);
    return t('pages.aiWorkout.routineNameFormat', { 
      muscleGroup: translatedMuscleGroup, 
      level: translatedLevel 
    });
  }
  
  // 기타 루틴 이름들
  const routineTranslations: { [key: string]: string } = {
    '초급 아침 루틴': 'pages.aiWorkout.routines.beginnerMorning',
    '중급 전신 운동 루틴': 'pages.aiWorkout.routines.intermediateFullBody',
    '중급 상체 집중 루틴': 'pages.aiWorkout.routines.intermediateUpperBody',
    '하체 초급 루틴': 'pages.aiWorkout.routines.beginnerLowerBody',
    '가슴 중급 루틴': 'pages.aiWorkout.routines.intermediateChest',
    '등 상급 루틴': 'pages.aiWorkout.routines.advancedBack',
    '하체 초급 기본 루틴': 'pages.aiWorkout.routines.beginnerLowerBodyBasic',
    '가슴 중급 기본 루틴': 'pages.aiWorkout.routines.intermediateChestBasic'
  };
  
  const translationKey = routineTranslations[name];
  if (translationKey) {
    return t(translationKey);
  }
  
  return name;
};
